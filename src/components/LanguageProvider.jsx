const LanguageContext = createContext();

export const LanguageProvider = ({ children }) => {
    // Load language from localStorage or default to 'he'
    const [language, setLanguageState] = useState(() => {
        try {
            return localStorage.getItem('app_language') || 'he';
        } catch {
            return 'he';
        }
    });

    // Save language to localStorage whenever it changes
    const setLanguage = (newLanguage) => {
        try {
            localStorage.setItem('app_language', newLanguage);
        } catch (error) {
            console.warn('Could not save language preference:', error);
        }
        setLanguageState(newLanguage);
    };

    // Apply RTL/LTR and lang attribute to document whenever language changes
    useEffect(() => {
        const isRTL = ['he', 'ar'].includes(language);
        document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
        document.documentElement.lang = language;
    }, [language]);

    const t = (key, params = {}) => {
        let translation = translations[language]?.[key] || translations['en']?.[key] || key;
        
        // Remove undefined entries from translation (like competitive imports)
        if (translation === undefined) {
            return key; // Fallback to key if translation is explicitly undefined
        }

        Object.keys(params).forEach(param => {
            translation = translation.replace(`{${param}}`, params[param]);
        });
        
        return translation;
    };

    return (
        <LanguageContext.Provider value={{ language, setLanguage, t }}>
            {children}
        </LanguageContext.Provider>
    );
};

export const useLanguage = () => {
    const context = useContext(LanguageContext);
    if (!context) {
        throw new Error('useLanguage must be used within a LanguageProvider');
    }
    return context;
};

// Helper: safe translation with Hebrew override to avoid English fallbacks
export const useSafeT = () => {
    const { t, language } = useLanguage();
    return (key, he, en) => {
        const v = t(key);
        if (language === 'he' && (v === key || !v)) return he;
        return (v === key || !v) ? (en ?? key) : v;
    };
};